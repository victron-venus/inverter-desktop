#!/bin/bash
# Monitor OSS-Fuzz results and generate reports

set -e

PROJECT_NAME="inverter-desktop"
OUTPUT_DIR="security-reports"
DATE=$(date +%Y-%m-%d)

# Create output directory
mkdir -p "$OUTPUT_DIR"

echo "Fetching OSS-Fuzz results for $PROJECT_NAME..."

# Check if project is integrated
if ! curl -s "https://oss-fuzz.com/testcases?project=$PROJECT_NAME" | grep -q "$PROJECT_NAME"; then
    echo "⚠️  Project not yet integrated with OSS-Fuzz"
    echo "Follow OSS_FUZZ_GUIDE.md for integration instructions"
    exit 1
fi

# Fetch test case statistics
echo "Fetching test case statistics..."
curl -s "https://oss-fuzz.com/testcases?project=$PROJECT_NAME" > "$OUTPUT_DIR/testcases_$DATE.html"

# Fetch coverage data if available
echo "Fetching coverage data..."
curl -s "https://introspector.oss-fuzz.com/?project=$PROJECT_NAME" > "$OUTPUT_DIR/coverage_$DATE.html"

# Generate summary report
cat > "$OUTPUT_DIR/summary_$DATE.md" << EOF
# OSS-Fuzz Security Report

**Project:** $PROJECT_NAME
**Date:** $DATE
**Status:** Integrated

## Statistics

- **Fuzz Targets:** 3
- **Test Cases:** $(curl -s "https://oss-fuzz.com/testcases?project=$PROJECT_NAME" | grep -oP '(?<=Total test cases: )\d+' || echo "N/A")
- **Coverage:** $(curl -s "https://introspector.oss-fuzz.com/?project=$PROJECT_NAME" | grep -oP '(?<=Coverage: )\d+%' || echo "N/A")

## Recent Findings

Check OSS-Fuzz dashboard for latest findings:
https://oss-fuzz.com/testcases?project=$PROJECT_NAME

## Coverage Analysis

Detailed coverage analysis:
https://introspector.oss-fuzz.com/?project=$PROJECT_NAME

## Recommendations

1. Review crash reports regularly
2. Add new fuzz targets for new features
3. Update corpus with interesting inputs
4. Monitor coverage trends

## Next Steps

- Fix any identified vulnerabilities
- Update SECURITY_STATUS.md with findings
- Consider adding additional fuzz targets

---

Generated by OSS-Fuzz monitoring script
EOF

echo "✅ Report generated: $OUTPUT_DIR/summary_$DATE.md"
echo "📊 Full data available in: $OUTPUT_DIR/"
echo ""
echo "View results online:"
echo "  https://oss-fuzz.com/testcases?project=$PROJECT_NAME"
echo "  https://introspector.oss-fuzz.com/?project=$PROJECT_NAME"
